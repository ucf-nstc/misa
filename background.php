<section class="container">
	<div class="row">
		<div class="col-md-8">
			<h2>HLB</h2>
			<ul>
				<li>
					<p>While Santra focused primarily on Citrus Canker, Huanglongbing (HLB), also known as Citrus Greening, became a bigger threat in Florida groves.</p>
				</li>
				<li>
					<p>HLB is a century old bacterial disease, which has devastated citrus groves globally, including major citrus producing countries (USA, Brazil and Mexico), causing billions of dollar crop loss every year.</p>
				</li>
				<li>
					<p>In Florida, <u>over 90% trees </u>are believed to be infected with HLB and orange production has dropped to 81.5 million boxes (2015-2016 season), a record low yield in the past 50 years. Texas and California citrus industry are just a few years behind Florida to becoming endemic.</p>
				</li>
				<li>
					<p>HLB causing bacteria, <em>Candidatus</em> Liberabacter asiaticus (<em>C</em>Las) is restricted to phloem tissue, thus requiring a therapy that must be delivered systemically to become effective.</p>
				</li>
				<li>
					<p>Therapeutic agent must be small enough to be mobilized systemically. The particulate sizes should be comparable to phloem proteins or even smaller so that they can easily travel between plant cells or through cellular pores. After the job is done, therapeutic agent must be decomposed and cleared through metabolic pathways so that it is safe to plant, the environment and ultimately human health.</p>
				</li>
				<li>
					<p>Santra conceived the idea of protein sized small (&lt;5.0 nm) particle laden therapeutic technology while attending the International Research Conference on Huanglongbing (IRCHLB) in 2013, leading to the invention of ZinkicideTM.</p>
				</li>
				<li>
					<p>Graham and Johnson evaluated field efficacy of ZinkicideTM. ZinkicideTM demonstrated unprecedented efficacy in grapefruit canker trial and HLB. Efforts are currently underway to register ZinkicideTM with US EPA.</p>
				</li>
			</ul>
			<p><a href="http://www.plantmanagementnetwork.org/pub/php/review/2007/huanglongbing/">To learn more about HLB, click here.</a></p>
		</div>
</section>


